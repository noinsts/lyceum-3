    if len(teacher_name) < 3:
        raise ValidationError("–î–æ–≤–∂–∏–Ω–∞ —ñ–º–µ–Ω—ñ –º–∞—î –±—É—Ç–∏ –±—ñ–ª—å—à–µ 3 —Å–∏–º–≤–æ–ª—ñ–≤")

    if len(teacher_name) > 50:
        raise ValidationError("–î–æ–≤–∂–∏–Ω–∞ —ñ–º–µ–Ω—ñ –º–∞—î –±—É—Ç–∏ –º–µ–Ω—à–µ 50 —Å–∏–º–≤–æ–ª—ñ–≤")

    match = TEACHER_NAME_PATTERN.match(teacher_name.strip())

    if not isinstance(teacher_name, str) or not match:
        raise ValidationError("–í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π—Ç–µ —Ñ–æ—Ä–º–∞—Ç \"–ü—Ä—ñ–∑–≤–∏—â–µ —ñ–º'—è –ø–æ-–±–∞—Ç—å–∫–æ–≤—ñ\"")

