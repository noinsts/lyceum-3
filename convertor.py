def get_schedule_for_class_day(class_name, day_of_week):
    cursor.execute("""
    SELECT lesson_number, time_period, subject 
    FROM schedule 
    WHERE class = ? AND day_of_week = ? 
    ORDER BY lesson_number
    """, (class_name, day_of_week))

    return cursor.fetchall()


